//! Rust source generation for validated analysis specs.
//!
//! This is intentionally a narrow first slice: it emits a single producer with
//! object cuts, count-based region requirements, `count(...)` outputs, and
//! `leading(...).pt`-style floating-point outputs.

use std::collections::{BTreeMap, BTreeSet};
use std::error::Error;
use std::fmt;
use std::fmt::Write as _;

use nano_core::BranchType;

use crate::{
    AnalysisSpec, ArithOp, CmpOp, Cut, DerivedObjectDef, DerivedSource, Expr, ModelDef,
    ObjectCandidateDef, ObjectDef, ObjectPairDef, PairConstraint, PairSelection, ResolvedPlan,
};

/// Generate compilable Rust producer source from a validated plan.
///
/// The caller should obtain the plan from [`crate::validate`]. Code generation
/// assumes validation has already resolved branch existence and semantic object
/// references; this layer rejects only forms outside the first codegen slice.
pub fn generate_producer_source(plan: &ResolvedPlan) -> Result<String, CodegenError> {
    Generator { plan }.generate()
}

#[derive(Debug, Clone, PartialEq, Eq)]
pub enum CodegenError {
    InvalidIdentifier { context: String, value: String },
    UnsupportedFeature(String),
}

impl fmt::Display for CodegenError {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        match self {
            Self::InvalidIdentifier { context, value } => {
                write!(f, "{context}: `{value}` is not a supported Rust identifier")
            }
            Self::UnsupportedFeature(detail) => f.write_str(detail),
        }
    }
}

impl Error for CodegenError {}

struct Generator<'a> {
    plan: &'a ResolvedPlan,
}

impl<'a> Generator<'a> {
    fn generate(&self) -> Result<String, CodegenError> {
        if self.spec().models.is_empty() {
            return self.generate_without_models();
        }
        self.generate_with_models()
    }

    fn generate_without_models(&self) -> Result<String, CodegenError> {
        self.validate_supported_spec()?;

        let mut source = String::new();
        writeln!(
            source,
            "// @generated by nano-spec from analysis `{}`. Do not edit by hand.",
            self.spec().name
        )
        .unwrap();
        self.emit_region_markers(&mut source)?;
        writeln!(source).unwrap();
        writeln!(source, "#[derive(Debug, Clone, Copy, PartialEq)]").unwrap();
        writeln!(source, "pub struct GenRow {{").unwrap();
        for output in &self.spec().outputs {
            writeln!(
                source,
                "    pub {}: {},",
                checked_ident(&output.name, "output field")?,
                self.output_type(&output.expr)?
            )
            .unwrap();
        }
        writeln!(source, "}}").unwrap();
        writeln!(source).unwrap();
        self.emit_derived_row_structs(&mut source)?;
        self.emit_kinematic_helpers(&mut source)?;
        writeln!(source, "pub struct GeneratedProducer;").unwrap();
        writeln!(source).unwrap();
        writeln!(source, "impl GeneratedProducer {{").unwrap();
        writeln!(
            source,
            "    pub fn analyze(event: &nano_core::Event) -> nano_core::Result<Option<GenRow>> {{"
        )
        .unwrap();

        for object in &self.spec().objects {
            self.emit_object_selection(&mut source, object)?;
        }
        self.emit_derived_objects(&mut source)?;

        if !self.spec().regions.is_empty() {
            writeln!(
                source,
                "        let baseline = nano_analysis::Ev::new(event)"
            )
            .unwrap();
            writeln!(source, "            .preselect(|_| true)").unwrap();
            writeln!(
                source,
                "            .expect(\"true generated preselection should always pass\");"
            )
            .unwrap();
            self.emit_region_selections(&mut source, "baseline")?;
        }

        for output in &self.spec().outputs {
            self.emit_required_output_unwrap(&mut source, &output.expr, &output.name)?;
        }

        writeln!(source, "        Ok(Some(GenRow {{").unwrap();
        for output in &self.spec().outputs {
            let field = checked_ident(&output.name, "output field")?;
            let expr = self.emit_output_expr(&output.expr, &field)?;
            if field == expr {
                writeln!(source, "            {field},").unwrap();
            } else {
                writeln!(source, "            {field}: {expr},").unwrap();
            }
        }
        writeln!(source, "        }}))").unwrap();
        writeln!(source, "    }}").unwrap();
        writeln!(source, "}}").unwrap();

        Ok(source)
    }

    fn generate_with_models(&self) -> Result<String, CodegenError> {
        self.validate_supported_spec()?;

        let mut source = String::new();
        writeln!(
            source,
            "// @generated by nano-spec from analysis `{}`. Do not edit by hand.",
            self.spec().name
        )
        .unwrap();
        self.emit_region_markers(&mut source)?;
        writeln!(source).unwrap();
        writeln!(
            source,
            "type GenResult<T> = std::result::Result<T, nano_inference::InferError>;"
        )
        .unwrap();
        writeln!(source).unwrap();
        writeln!(
            source,
            "fn gen_core_error(error: impl std::fmt::Display) -> nano_inference::InferError {{"
        )
        .unwrap();
        writeln!(
            source,
            "    nano_inference::InferError::Feature(error.to_string())"
        )
        .unwrap();
        writeln!(source, "}}").unwrap();

        for model in &self.spec().models {
            self.emit_model_tag(&mut source, model)?;
        }

        writeln!(source).unwrap();
        writeln!(source, "#[derive(Debug, Clone, Copy, PartialEq)]").unwrap();
        writeln!(source, "pub struct GenRow {{").unwrap();
        for output in &self.spec().outputs {
            writeln!(
                source,
                "    pub {}: {},",
                checked_ident(&output.name, "output field")?,
                self.output_type(&output.expr)?
            )
            .unwrap();
        }
        writeln!(source, "}}").unwrap();
        writeln!(source).unwrap();
        writeln!(source, "pub struct GeneratedProducer;").unwrap();
        writeln!(source).unwrap();
        writeln!(source, "impl GeneratedProducer {{").unwrap();
        writeln!(
            source,
            "    pub fn analyze(event: &nano_core::Event, predictor: &impl nano_inference::Predictor) -> GenResult<Option<GenRow>> {{"
        )
        .unwrap();

        for model in &self.spec().models {
            self.emit_model_inference(&mut source, model)?;
        }
        let baseline_ident = self
            .spec()
            .models
            .first()
            .map(|model| checked_ident(&model.name, "model name"))
            .transpose()?
            .map(|model_ident| format!("{model_ident}_baseline"));

        let leading_region_exprs = self.leading_region_exprs();
        for object in &self.spec().objects {
            self.emit_model_object_selection(&mut source, &object.name)?;
        }

        let mut unwrapped_leading = BTreeSet::new();
        for region in &self.spec().regions {
            let mut conditions = Vec::with_capacity(region.require.len());
            for requirement in &region.require {
                let condition = self.emit_model_region_requirement(
                    &requirement.lhs,
                    requirement.op,
                    requirement.rhs.value,
                    &mut source,
                    &mut unwrapped_leading,
                )?;
                conditions.push(condition);
            }
            if let Some(baseline_ident) = &baseline_ident {
                self.emit_region_selection(&mut source, &region.name, baseline_ident, &conditions)?;
            }
        }

        for output in &self.spec().outputs {
            if let Expr::LeadingAttr { object, attr } = &output.expr {
                let key = (object.clone(), attr.clone());
                if leading_region_exprs.contains(&key) {
                    continue;
                }
                let ident = leading_attr_ident(object, attr)?;
                writeln!(source, "        let Some({ident}) = {ident} else {{").unwrap();
                writeln!(source, "            return Ok(None);").unwrap();
                writeln!(source, "        }};").unwrap();
            }
        }

        writeln!(source, "        Ok(Some(GenRow {{").unwrap();
        for output in &self.spec().outputs {
            let field = checked_ident(&output.name, "output field")?;
            let expr = self.emit_model_output_expr(&output.expr)?;
            if field == expr {
                writeln!(source, "            {field},").unwrap();
            } else {
                writeln!(source, "            {field}: {expr},").unwrap();
            }
        }
        writeln!(source, "        }}))").unwrap();
        writeln!(source, "    }}").unwrap();
        writeln!(source, "}}").unwrap();

        Ok(source)
    }

    fn spec(&self) -> &AnalysisSpec {
        &self.plan.spec
    }

    fn validate_supported_spec(&self) -> Result<(), CodegenError> {
        if !self.spec().models.is_empty() && !self.spec().derived_objects.is_empty() {
            return Err(CodegenError::UnsupportedFeature(
                "derived objects are not yet supported by model-aware codegen".to_string(),
            ));
        }

        for object in &self.spec().objects {
            checked_ident(&object.name, "object name")?;
            checked_ident(&object.source, "object source")?;
            for cut in &object.cuts {
                self.validate_cut_expr(&object.name, &cut.lhs)?;
            }
        }

        for derived in &self.spec().derived_objects {
            checked_ident(&derived.name, "derived object name")?;
            self.validate_derived_object(derived)?;
        }

        for region in &self.spec().regions {
            checked_ident(&region.name, "region name")?;
            for requirement in &region.require {
                if self.spec().models.is_empty() {
                    self.validate_region_expr(&requirement.lhs, "region requirement")?;
                    if matches!(requirement.lhs, Expr::Count(_)) {
                        checked_count_rhs(requirement.rhs.value, "region requirement")?;
                    }
                } else {
                    self.validate_model_region_expr(&requirement.lhs, "region requirement")?;
                }
            }
        }

        for output in &self.spec().outputs {
            checked_ident(&output.name, "output name")?;
            match &output.expr {
                Expr::Count(object) => {
                    self.object(object)?;
                }
                Expr::CountWhere { object, .. } => {
                    self.object(object)?;
                }
                Expr::SumAttr { object, attr } => {
                    self.object(object)?;
                    self.require_supported_object_attr(object, attr, "sum output")?;
                }
                Expr::All { object, .. } | Expr::Any { object, .. } => {
                    self.object(object)?;
                }
                Expr::EitherPairPt { left, right, .. } => {
                    self.object(left)?;
                    self.object(right)?;
                    self.require_f32_attr(left, "pt", "either_pair_pt output")?;
                    self.require_f32_attr(right, "pt", "either_pair_pt output")?;
                }
                Expr::ClosestMass { left, right, .. } | Expr::OtherMass { left, right, .. } => {
                    self.validate_derived_attr(left, "mass", "mass-order output")?;
                    self.validate_derived_attr(right, "mass", "mass-order output")?;
                }
                Expr::Attr { object, attr } if self.derived_object(object).is_ok() => {
                    self.validate_derived_attr(object, attr, "derived output")?;
                }
                Expr::LeadingAttr { object, attr } => {
                    if self.spec().models.is_empty() && attr != "pt" {
                        return Err(CodegenError::UnsupportedFeature(format!(
                            "leading output `{}` uses `{attr}`; this slice only supports leading(...).pt",
                            output.name
                        )));
                    }
                    self.require_f32_attr(object, attr, "leading output")?;
                }
                other => {
                    return Err(CodegenError::UnsupportedFeature(format!(
                        "output `{}` uses unsupported expression `{other}`",
                        output.name
                    )));
                }
            }
        }

        for model in &self.spec().models {
            checked_ident(&model.name, "model name")?;
            self.model_type_ident(model)?;
            self.model_batch_source(model)?;
            let (_, output_attr) = split_model_output(&model.output)?;
            checked_ident(output_attr, "model output attribute")?;
            for input in &model.inputs {
                self.validate_model_input(model, input)?;
            }
        }

        Ok(())
    }

    fn emit_region_markers(&self, source: &mut String) -> Result<(), CodegenError> {
        for region in &self.spec().regions {
            let type_ident = region_type_ident(&region.name)?;
            writeln!(source).unwrap();
            writeln!(
                source,
                "#[derive(Debug, Clone, Copy, Default, PartialEq, Eq)]"
            )
            .unwrap();
            writeln!(source, "struct {type_ident};").unwrap();
            writeln!(source).unwrap();
            writeln!(source, "impl nano_analysis::Region for {type_ident} {{").unwrap();
            writeln!(
                source,
                "    const NAME: &'static str = {};",
                rust_string(&region.name)
            )
            .unwrap();
            writeln!(source, "}}").unwrap();
        }
        Ok(())
    }

    fn emit_region_selections(
        &self,
        source: &mut String,
        baseline_ident: &str,
    ) -> Result<(), CodegenError> {
        for derived_name in self.derived_region_objects() {
            self.emit_derived_unwrap(source, &derived_name)?;
        }
        for region in &self.spec().regions {
            let conditions = region
                .require
                .iter()
                .map(|requirement| {
                    self.emit_region_requirement(
                        &requirement.lhs,
                        requirement.op,
                        requirement.rhs.value,
                    )
                })
                .collect::<Result<Vec<_>, _>>()?;
            self.emit_region_selection(source, &region.name, baseline_ident, &conditions)?;
        }
        Ok(())
    }

    fn emit_required_output_unwrap(
        &self,
        source: &mut String,
        expr: &Expr,
        output_name: &str,
    ) -> Result<(), CodegenError> {
        match expr {
            Expr::LeadingAttr { .. } => {
                let name = checked_ident(output_name, "leading output")?;
                writeln!(source, "        let Some({name}) = {name} else {{").unwrap();
                writeln!(source, "            return Ok(None);").unwrap();
                writeln!(source, "        }};").unwrap();
                Ok(())
            }
            Expr::Attr { object, .. }
                if self.derived_object(object).is_ok()
                    && !self.derived_region_objects().contains(object) =>
            {
                self.emit_derived_unwrap(source, object)
            }
            _ => Ok(()),
        }
    }

    fn emit_derived_unwrap(
        &self,
        source: &mut String,
        derived_name: &str,
    ) -> Result<(), CodegenError> {
        let ident = checked_ident(derived_name, "derived object")?;
        writeln!(source, "        let Some({ident}) = {ident} else {{").unwrap();
        writeln!(source, "            return Ok(None);").unwrap();
        writeln!(source, "        }};").unwrap();
        Ok(())
    }

    fn derived_region_objects(&self) -> BTreeSet<String> {
        let mut objects = BTreeSet::new();
        for region in &self.spec().regions {
            for requirement in &region.require {
                collect_derived_objects_in_expr(&requirement.lhs, self.spec(), &mut objects);
            }
        }
        objects
    }

    fn emit_region_selection(
        &self,
        source: &mut String,
        region_name: &str,
        baseline_ident: &str,
        conditions: &[String],
    ) -> Result<(), CodegenError> {
        let type_ident = region_type_ident(region_name)?;
        let event_ident = checked_ident(region_name, "region event")?;
        let predicate = if conditions.is_empty() {
            "true".to_string()
        } else {
            conditions.join(" && ")
        };
        writeln!(
            source,
            "        let Some(_{event_ident}_event) = {baseline_ident}.select::<{type_ident}>(|_| {predicate}) else {{"
        )
        .unwrap();
        writeln!(source, "            return Ok(None);").unwrap();
        writeln!(source, "        }};").unwrap();
        Ok(())
    }

    fn emit_object_selection(
        &self,
        source: &mut String,
        object: &ObjectDef,
    ) -> Result<(), CodegenError> {
        let object_name = checked_ident(&object.name, "object name")?;
        let collection_ident = format!("{object_name}_collection");
        let item_ident = format!("{object_name}_item");
        let count_ident = if self.count_needed_for_object(&object.name) {
            count_ident(&object.name)?
        } else {
            format!("_{}", count_ident(&object.name)?)
        };
        let attrs = self.attrs_needed_for_object(&object.name)?;
        let selected_attrs = self.selected_attrs_for_object(&object.name)?;
        let selected_type = selected_type_ident(&object.name)?;
        let selected_ident = selected_objects_ident(&object.name)?;

        if !selected_attrs.is_empty() {
            writeln!(source, "        #[derive(Debug, Clone, Copy)]").unwrap();
            writeln!(source, "        struct {selected_type} {{").unwrap();
            writeln!(source, "            source_index: usize,").unwrap();
            for attr in &selected_attrs {
                writeln!(
                    source,
                    "            {}: {},",
                    checked_ident(attr, "selected object attribute")?,
                    self.object_attr_rust_type(&object.name, attr)?
                )
                .unwrap();
            }
            writeln!(source, "        }}").unwrap();
            writeln!(source, "        let mut {selected_ident} = Vec::new();").unwrap();
        }

        writeln!(
            source,
            "        let {collection_ident} = event.collection({})?;",
            rust_string(&object.source)
        )
        .unwrap();
        writeln!(source, "        let mut {count_ident} = 0_u32;").unwrap();

        for output in &self.spec().outputs {
            if let Expr::LeadingAttr {
                object: output_object,
                ..
            } = &output.expr
            {
                if output_object == &object.name {
                    let output_name = checked_ident(&output.name, "leading output")?;
                    writeln!(source, "        let mut {output_name}: Option<f32> = None;").unwrap();
                }
            }
        }

        writeln!(source).unwrap();
        writeln!(
            source,
            "        for {item_ident} in {collection_ident}.iter() {{"
        )
        .unwrap();

        for attr in attrs {
            self.require_supported_object_attr(&object.name, &attr, "object attribute")?;
            let attr_ident = attr_ident(&object.name, &attr)?;
            let rust_type = self.object_attr_rust_type(&object.name, &attr)?;
            writeln!(
                source,
                "            let {attr_ident} = {item_ident}.get::<{rust_type}>({})?;",
                rust_string(&attr)
            )
            .unwrap();
        }

        let condition = if object.cuts.is_empty() {
            "true".to_string()
        } else {
            object
                .cuts
                .iter()
                .map(|cut| self.emit_cut(object, cut))
                .collect::<Result<Vec<_>, _>>()?
                .join(" && ")
        };

        writeln!(source, "            if {condition} {{").unwrap();
        writeln!(source, "                {count_ident} += 1;").unwrap();

        for output in &self.spec().outputs {
            if let Expr::LeadingAttr {
                object: output_object,
                attr,
            } = &output.expr
            {
                if output_object == &object.name {
                    let output_name = checked_ident(&output.name, "leading output")?;
                    let attr_ident = attr_ident(&object.name, attr)?;
                    writeln!(source, "                {output_name} = Some(").unwrap();
                    writeln!(
                        source,
                        "                    {output_name}.map_or({attr_ident}, |lead| lead.max({attr_ident})),"
                    )
                    .unwrap();
                    writeln!(source, "                );").unwrap();
                }
            }
        }
        if !selected_attrs.is_empty() {
            writeln!(
                source,
                "                {selected_ident}.push({selected_type} {{"
            )
            .unwrap();
            writeln!(
                source,
                "                    source_index: {item_ident}.index(),"
            )
            .unwrap();
            for attr in &selected_attrs {
                let attr_ident = attr_ident(&object.name, attr)?;
                writeln!(
                    source,
                    "                    {}: {attr_ident},",
                    checked_ident(attr, "selected object attribute")?
                )
                .unwrap();
            }
            writeln!(source, "                }});").unwrap();
        }

        writeln!(source, "            }}").unwrap();
        writeln!(source, "        }}").unwrap();
        writeln!(source).unwrap();
        Ok(())
    }

    fn count_needed_for_object(&self, object_name: &str) -> bool {
        self.spec()
            .outputs
            .iter()
            .any(|output| matches!(&output.expr, Expr::Count(object) if object == object_name))
            || self.spec().regions.iter().any(|region| {
                region
                    .require
                    .iter()
                    .any(|requirement| expr_counts_object(&requirement.lhs, object_name))
            })
    }

    fn attrs_needed_for_object(&self, object_name: &str) -> Result<BTreeSet<String>, CodegenError> {
        let mut attrs = BTreeSet::new();
        let object = self.object(object_name)?;
        for cut in &object.cuts {
            collect_attr_names_for_current_object(&cut.lhs, object_name, &mut attrs)?;
        }
        for region in &self.spec().regions {
            for requirement in &region.require {
                collect_selected_attr_names(&requirement.lhs, object_name, &mut attrs)?;
            }
        }
        for output in &self.spec().outputs {
            collect_selected_attr_names(&output.expr, object_name, &mut attrs)?;
        }
        for output in &self.spec().outputs {
            if let Expr::LeadingAttr { object, attr } = &output.expr {
                if object == object_name {
                    attrs.insert(attr.clone());
                }
            }
        }
        for attr in self.selected_attrs_for_object(object_name)? {
            attrs.insert(attr);
        }
        if !self.spec().models.is_empty() {
            for region in &self.spec().regions {
                for requirement in &region.require {
                    if let Expr::LeadingAttr { object, attr } = &requirement.lhs {
                        if object == object_name {
                            attrs.insert(attr.clone());
                        }
                    }
                }
            }
        }
        Ok(attrs)
    }

    fn selected_attrs_for_object(
        &self,
        object_name: &str,
    ) -> Result<BTreeSet<String>, CodegenError> {
        let mut attrs = BTreeSet::new();
        for region in &self.spec().regions {
            for requirement in &region.require {
                collect_selected_attr_names(&requirement.lhs, object_name, &mut attrs)?;
            }
        }
        for output in &self.spec().outputs {
            collect_selected_attr_names(&output.expr, object_name, &mut attrs)?;
        }
        for derived in &self.spec().derived_objects {
            match &derived.source {
                DerivedSource::Pair(pair) => {
                    if pair.object != object_name {
                        continue;
                    }

                    attrs.insert("pt".to_string());
                    attrs.insert("eta".to_string());
                    attrs.insert("phi".to_string());
                    attrs.insert("mass".to_string());
                    for constraint in &pair.constraints {
                        if matches!(constraint, PairConstraint::OppositeCharge) {
                            attrs.insert("charge".to_string());
                        }
                    }
                    for filter in &pair.filters {
                        collect_pair_filter_attr_names(&filter.lhs, &mut attrs);
                    }
                }
                DerivedSource::Candidate(candidate) => {
                    if candidate.items.iter().any(|item| item == object_name) {
                        attrs.insert("pt".to_string());
                        attrs.insert("eta".to_string());
                        attrs.insert("phi".to_string());
                        attrs.insert("mass".to_string());
                        for filter in &candidate.filters {
                            collect_candidate_filter_attr_names(&filter.lhs, &mut attrs);
                        }
                    }
                }
            }
        }
        Ok(attrs)
    }

    fn emit_derived_row_structs(&self, source: &mut String) -> Result<(), CodegenError> {
        if self.spec().derived_objects.is_empty() {
            return Ok(());
        }

        for derived in &self.spec().derived_objects {
            let type_ident = derived_type_ident(&derived.name)?;
            writeln!(source, "#[derive(Debug, Clone, Copy, PartialEq)]").unwrap();
            writeln!(source, "struct {type_ident} {{").unwrap();
            writeln!(source, "    mass: f64,").unwrap();
            writeln!(source, "    pt: f64,").unwrap();
            writeln!(source, "    min_delta_r: f64,").unwrap();
            writeln!(source, "    energy: f64,").unwrap();
            writeln!(source, "    px: f64,").unwrap();
            writeln!(source, "    py: f64,").unwrap();
            writeln!(source, "    pz: f64,").unwrap();
            writeln!(source, "    constituents: &'static [GenConstituent],").unwrap();
            writeln!(source, "}}").unwrap();
            writeln!(source).unwrap();
        }
        Ok(())
    }

    fn emit_kinematic_helpers(&self, source: &mut String) -> Result<(), CodegenError> {
        if self.spec().derived_objects.is_empty() {
            return Ok(());
        }

        writeln!(source, "#[derive(Debug, Clone, Copy, PartialEq)]").unwrap();
        writeln!(source, "struct GenConstituent {{").unwrap();
        writeln!(source, "    object: &'static str,").unwrap();
        writeln!(source, "    index: usize,").unwrap();
        writeln!(source, "    pt: f32,").unwrap();
        writeln!(source, "    eta: f32,").unwrap();
        writeln!(source, "    phi: f32,").unwrap();
        writeln!(source, "}}").unwrap();
        writeln!(source).unwrap();
        writeln!(
            source,
            "fn gen_four_vector(pt: f32, eta: f32, phi: f32, mass: f32) -> (f64, f64, f64, f64) {{"
        )
        .unwrap();
        writeln!(source, "    let pt = f64::from(pt);").unwrap();
        writeln!(source, "    let eta = f64::from(eta);").unwrap();
        writeln!(source, "    let phi = f64::from(phi);").unwrap();
        writeln!(source, "    let mass = f64::from(mass);").unwrap();
        writeln!(source, "    let px = pt * phi.cos();").unwrap();
        writeln!(source, "    let py = pt * phi.sin();").unwrap();
        writeln!(source, "    let pz = pt * eta.sinh();").unwrap();
        writeln!(
            source,
            "    let energy = (px * px + py * py + pz * pz + mass * mass).sqrt();"
        )
        .unwrap();
        writeln!(source, "    (energy, px, py, pz)").unwrap();
        writeln!(source, "}}").unwrap();
        writeln!(source).unwrap();
        writeln!(
            source,
            "fn gen_mass_pt(energy: f64, px: f64, py: f64, pz: f64) -> (f64, f64) {{"
        )
        .unwrap();
        writeln!(source, "    (").unwrap();
        writeln!(
            source,
            "        (energy * energy - px * px - py * py - pz * pz).max(0.0).sqrt(),"
        )
        .unwrap();
        writeln!(source, "        (px * px + py * py).sqrt(),").unwrap();
        writeln!(source, "    )").unwrap();
        writeln!(source, "}}").unwrap();
        writeln!(source).unwrap();
        writeln!(
            source,
            "fn gen_delta_r(left_eta: f32, left_phi: f32, right_eta: f32, right_phi: f32) -> f64 {{"
        )
        .unwrap();
        writeln!(source, "    let deta = f64::from(left_eta - right_eta);").unwrap();
        writeln!(
            source,
            "    let mut dphi = f64::from(left_phi - right_phi);"
        )
        .unwrap();
        writeln!(
            source,
            "    while dphi > std::f64::consts::PI {{ dphi -= 2.0 * std::f64::consts::PI; }}"
        )
        .unwrap();
        writeln!(
            source,
            "    while dphi <= -std::f64::consts::PI {{ dphi += 2.0 * std::f64::consts::PI; }}"
        )
        .unwrap();
        writeln!(source, "    (deta * deta + dphi * dphi).sqrt()").unwrap();
        writeln!(source, "}}").unwrap();
        writeln!(source).unwrap();
        writeln!(
            source,
            "fn gen_candidate_leading_pt(items: &[GenConstituent]) -> f32 {{"
        )
        .unwrap();
        writeln!(
            source,
            "    items.iter().map(|item| item.pt).fold(f32::NEG_INFINITY, f32::max)"
        )
        .unwrap();
        writeln!(source, "}}").unwrap();
        writeln!(source).unwrap();
        writeln!(
            source,
            "fn gen_candidate_subleading_pt(items: &[GenConstituent]) -> f32 {{"
        )
        .unwrap();
        writeln!(
            source,
            "    let mut pts = items.iter().map(|item| item.pt).collect::<Vec<_>>();"
        )
        .unwrap();
        writeln!(
            source,
            "    pts.sort_by(|left, right| right.total_cmp(left));"
        )
        .unwrap();
        writeln!(
            source,
            "    pts.get(1).copied().unwrap_or(f32::NEG_INFINITY)"
        )
        .unwrap();
        writeln!(source, "}}").unwrap();
        writeln!(source).unwrap();
        writeln!(
            source,
            "fn gen_candidate_min_delta_r(items: &[GenConstituent]) -> f64 {{"
        )
        .unwrap();
        writeln!(source, "    let mut min = f64::INFINITY;").unwrap();
        writeln!(
            source,
            "    for (left_pos, left) in items.iter().enumerate() {{"
        )
        .unwrap();
        writeln!(source, "        for right in &items[left_pos + 1..] {{").unwrap();
        writeln!(
            source,
            "            min = min.min(gen_delta_r(left.eta, left.phi, right.eta, right.phi));"
        )
        .unwrap();
        writeln!(source, "        }}").unwrap();
        writeln!(source, "    }}").unwrap();
        writeln!(source, "    min").unwrap();
        writeln!(source, "}}").unwrap();
        writeln!(source).unwrap();
        Ok(())
    }

    fn emit_derived_objects(&self, source: &mut String) -> Result<(), CodegenError> {
        for derived in self.ordered_derived_objects()? {
            match &derived.source {
                DerivedSource::Pair(pair) => {
                    self.emit_pair_derived_object(source, derived, pair)?;
                }
                DerivedSource::Candidate(candidate) => {
                    self.emit_candidate_derived_object(source, derived, candidate)?;
                }
            }
        }
        Ok(())
    }

    fn emit_pair_derived_object(
        &self,
        source: &mut String,
        derived: &DerivedObjectDef,
        pair: &ObjectPairDef,
    ) -> Result<(), CodegenError> {
        let derived_ident = checked_ident(&derived.name, "derived object")?;
        let derived_type = derived_type_ident(&derived.name)?;
        let selected_ident = selected_objects_ident(&pair.object)?;
        let order_ident = format!("{derived_ident}_order");
        let best_diff_ident = format!("{derived_ident}_best_diff");
        let excluded_ident = format!("{derived_ident}_excluded");
        let target = match &pair.selection {
            PairSelection::LeadingPt | PairSelection::NearestMassTruncated { .. } => None,
            PairSelection::NearestMass { target } => Some(f64_literal(target.value)),
        };
        let truncated_target = match &pair.selection {
            PairSelection::NearestMassTruncated { target } => Some(f64_literal(target.value)),
            PairSelection::LeadingPt | PairSelection::NearestMass { .. } => None,
        };

        if pair.exclude.is_empty() {
            writeln!(
                source,
                "        let {excluded_ident}: Vec<usize> = Vec::new();"
            )
            .unwrap();
        } else {
            writeln!(source, "        let mut {excluded_ident} = Vec::new();").unwrap();
        }
        for excluded in &pair.exclude {
            let excluded_ident_name = checked_ident(excluded, "excluded derived object")?;
            writeln!(
                source,
                "        if let Some(excluded) = &{excluded_ident_name} {{"
            )
            .unwrap();
            writeln!(
                source,
                "            {excluded_ident}.extend(excluded.constituents.iter().filter(|item| item.object == {}).map(|item| item.index));",
                rust_string(&pair.object)
            )
            .unwrap();
            writeln!(source, "        }}").unwrap();
        }
        if truncated_target.is_none() {
            writeln!(
                source,
                "        let mut {order_ident} = (0..{selected_ident}.len()).collect::<Vec<_>>();"
            )
            .unwrap();
            writeln!(
                source,
                "        {order_ident}.sort_by(|&left, &right| {selected_ident}[right].pt.total_cmp(&{selected_ident}[left].pt));"
            )
            .unwrap();
        } else {
            writeln!(
                source,
                "        let {order_ident} = (0..{selected_ident}.len()).collect::<Vec<_>>();"
            )
            .unwrap();
        }
        writeln!(
            source,
            "        let mut {derived_ident}: Option<{derived_type}> = None;"
        )
        .unwrap();
        if target.is_some() {
            writeln!(
                source,
                "        let mut {best_diff_ident}: Option<f64> = None;"
            )
            .unwrap();
        }
        if truncated_target.is_some() {
            writeln!(
                source,
                "        let mut {derived_ident}_best_mass = -1_i32;"
            )
            .unwrap();
        }
        let loop_label = format!("'{}_pairs", derived_ident);
        if target.is_some() || truncated_target.is_some() {
            writeln!(
                source,
                "        for (left_pos, &left) in {order_ident}.iter().enumerate() {{"
            )
            .unwrap();
        } else {
            writeln!(
                source,
                "        {loop_label}: for (left_pos, &left) in {order_ident}.iter().enumerate() {{"
            )
            .unwrap();
        }
        writeln!(
            source,
            "            for &right in &{order_ident}[left_pos + 1..] {{"
        )
        .unwrap();
        writeln!(
            source,
            "                let first = {selected_ident}[left];"
        )
        .unwrap();
        writeln!(
            source,
            "                let second = {selected_ident}[right];"
        )
        .unwrap();
        writeln!(
            source,
            "                if {excluded_ident}.contains(&first.source_index) || {excluded_ident}.contains(&second.source_index) {{"
        )
        .unwrap();
        writeln!(source, "                    continue;").unwrap();
        writeln!(source, "                }}").unwrap();

        for constraint in &pair.constraints {
            match constraint {
                PairConstraint::OppositeCharge => {
                    writeln!(
                        source,
                        "                if first.charge * second.charge >= 0 {{"
                    )
                    .unwrap();
                    writeln!(source, "                    continue;").unwrap();
                    writeln!(source, "                }}").unwrap();
                }
                PairConstraint::SameFlavor => {}
            }
        }

        for filter in &pair.filters {
            let lhs = self.emit_pair_filter_expr(&filter.lhs)?;
            writeln!(
                source,
                "                if !({lhs} {} {}) {{",
                cmp_op(filter.op),
                numeric_literal_for_expr(&filter.lhs, filter.rhs.value)
            )
            .unwrap();
            writeln!(source, "                    continue;").unwrap();
            writeln!(source, "                }}").unwrap();
        }

        writeln!(
            source,
            "                let (e1, px1, py1, pz1) = gen_four_vector(first.pt, first.eta, first.phi, first.mass);"
        )
        .unwrap();
        writeln!(
            source,
            "                let (e2, px2, py2, pz2) = gen_four_vector(second.pt, second.eta, second.phi, second.mass);"
        )
        .unwrap();
        writeln!(source, "                let energy = e1 + e2;").unwrap();
        writeln!(source, "                let px = px1 + px2;").unwrap();
        writeln!(source, "                let py = py1 + py2;").unwrap();
        writeln!(source, "                let pz = pz1 + pz2;").unwrap();
        writeln!(
            source,
            "                let (mass, pt) = gen_mass_pt(energy, px, py, pz);"
        )
        .unwrap();
        writeln!(
            source,
            "                if !mass.is_finite() || mass <= 0.0 {{"
        )
        .unwrap();
        writeln!(source, "                    continue;").unwrap();
        writeln!(source, "                }}").unwrap();
        writeln!(
            source,
            "                let constituents = Box::leak(Box::new(["
        )
        .unwrap();
        writeln!(
            source,
            "                    GenConstituent {{ object: {}, index: first.source_index, pt: first.pt, eta: first.eta, phi: first.phi }},",
            rust_string(&pair.object)
        )
        .unwrap();
        writeln!(
            source,
            "                    GenConstituent {{ object: {}, index: second.source_index, pt: second.pt, eta: second.eta, phi: second.phi }},",
            rust_string(&pair.object)
        )
        .unwrap();
        writeln!(source, "                ]));").unwrap();
        writeln!(
            source,
            "                let min_delta_r = gen_delta_r(first.eta, first.phi, second.eta, second.phi);"
        )
        .unwrap();
        writeln!(
            source,
            "                let candidate = {derived_type} {{ mass, pt, min_delta_r, energy, px, py, pz, constituents }};"
        )
        .unwrap();
        match (target, truncated_target) {
            (None, None) => {
                writeln!(source, "                {derived_ident} = Some(candidate);").unwrap();
                writeln!(source, "                break {loop_label};").unwrap();
            }
            (Some(target), None) => {
                writeln!(
                    source,
                    "                let diff = (mass - {target}).abs();"
                )
                .unwrap();
                writeln!(
                    source,
                    "                if {best_diff_ident}.is_none_or(|best| diff < best) {{"
                )
                .unwrap();
                writeln!(
                    source,
                    "                    {best_diff_ident} = Some(diff);"
                )
                .unwrap();
                writeln!(
                    source,
                    "                    {derived_ident} = Some(candidate);"
                )
                .unwrap();
                writeln!(source, "                }}").unwrap();
            }
            (None, Some(target)) => {
                writeln!(
                    source,
                    "                if ({target} - mass).abs() < ({target} - f64::from({derived_ident}_best_mass)).abs() {{"
                )
                .unwrap();
                writeln!(
                    source,
                    "                    {derived_ident}_best_mass = mass as i32;"
                )
                .unwrap();
                writeln!(
                    source,
                    "                    {derived_ident} = Some(candidate);"
                )
                .unwrap();
                writeln!(source, "                }}").unwrap();
            }
            (Some(_), Some(_)) => unreachable!("pair selection cannot use both nearest modes"),
        }
        writeln!(source, "            }}").unwrap();
        writeln!(source, "        }}").unwrap();
        writeln!(source).unwrap();
        Ok(())
    }

    fn emit_pair_filter_expr(&self, expr: &Expr) -> Result<String, CodegenError> {
        match expr {
            Expr::PairDeltaR => {
                Ok("gen_delta_r(first.eta, first.phi, second.eta, second.phi)".to_string())
            }
            Expr::PairLeadingPt => Ok("first.pt.max(second.pt)".to_string()),
            Expr::PairSubleadingPt => Ok("first.pt.min(second.pt)".to_string()),
            other => Err(CodegenError::UnsupportedFeature(format!(
                "pair filter expression `{other}` is not supported by codegen"
            ))),
        }
    }

    fn emit_candidate_derived_object(
        &self,
        source: &mut String,
        derived: &DerivedObjectDef,
        candidate: &ObjectCandidateDef,
    ) -> Result<(), CodegenError> {
        let derived_ident = checked_ident(&derived.name, "derived object")?;
        let derived_type = derived_type_ident(&derived.name)?;
        let used_ident = format!("{derived_ident}_used");

        writeln!(
            source,
            "        let mut {derived_ident}: Option<{derived_type}> = None;"
        )
        .unwrap();
        writeln!(
            source,
            "        let mut {used_ident}: Vec<GenConstituent> = Vec::new();"
        )
        .unwrap();
        writeln!(source, "        let mut energy = 0.0_f64;").unwrap();
        writeln!(source, "        let mut px = 0.0_f64;").unwrap();
        writeln!(source, "        let mut py = 0.0_f64;").unwrap();
        writeln!(source, "        let mut pz = 0.0_f64;").unwrap();
        writeln!(source, "        let mut candidate_complete = true;").unwrap();

        let mut object_occurrences = BTreeMap::<String, usize>::new();
        for item in &candidate.items {
            if self.object(item).is_ok() {
                let occurrence = object_occurrences.entry(item.clone()).or_insert(0);
                let source_ident = selected_objects_ident(item)?;
                let local_ident = format!("{derived_ident}_{item}_{occurrence}");
                writeln!(source, "        if candidate_complete {{").unwrap();
                writeln!(
                    source,
                    "            if let Some({local_ident}) = {object_getter}.copied() {{",
                    object_getter = if *occurrence == 0 {
                        format!("{source_ident}.first()")
                    } else {
                        format!("{source_ident}.get({occurrence})")
                    }
                )
                .unwrap();
                writeln!(
                    source,
                    "                let (item_e, item_px, item_py, item_pz) = gen_four_vector({local_ident}.pt, {local_ident}.eta, {local_ident}.phi, {local_ident}.mass);"
                )
                .unwrap();
                writeln!(source, "                energy += item_e;").unwrap();
                writeln!(source, "                px += item_px;").unwrap();
                writeln!(source, "                py += item_py;").unwrap();
                writeln!(source, "                pz += item_pz;").unwrap();
                writeln!(
                    source,
                    "                {used_ident}.push(GenConstituent {{ object: {}, index: {local_ident}.source_index, pt: {local_ident}.pt, eta: {local_ident}.eta, phi: {local_ident}.phi }});",
                    rust_string(item)
                )
                .unwrap();
                writeln!(source, "            }} else {{").unwrap();
                writeln!(source, "                candidate_complete = false;").unwrap();
                writeln!(source, "            }}").unwrap();
                writeln!(source, "        }}").unwrap();
                *occurrence += 1;
            } else {
                self.derived_object(item)?;
                let item_ident = checked_ident(item, "candidate item")?;
                writeln!(source, "        if candidate_complete {{").unwrap();
                writeln!(
                    source,
                    "            if let Some({item_ident}) = &{item_ident} {{"
                )
                .unwrap();
                writeln!(source, "                energy += {item_ident}.energy;").unwrap();
                writeln!(source, "                px += {item_ident}.px;").unwrap();
                writeln!(source, "                py += {item_ident}.py;").unwrap();
                writeln!(source, "                pz += {item_ident}.pz;").unwrap();
                writeln!(
                    source,
                    "                {used_ident}.extend({item_ident}.constituents.iter().copied());"
                )
                .unwrap();
                writeln!(source, "            }} else {{").unwrap();
                writeln!(source, "                candidate_complete = false;").unwrap();
                writeln!(source, "            }}").unwrap();
                writeln!(source, "        }}").unwrap();
            }
        }
        writeln!(source, "        if candidate_complete {{").unwrap();
        writeln!(
            source,
            "            let (mass, pt) = gen_mass_pt(energy, px, py, pz);"
        )
        .unwrap();
        writeln!(
            source,
            "            let min_delta_r = gen_candidate_min_delta_r(&{used_ident});"
        )
        .unwrap();
        writeln!(source, "            if mass.is_finite() && mass > 0.0 {{").unwrap();
        for filter in &candidate.filters {
            let lhs = self.emit_candidate_filter_expr(&filter.lhs, &used_ident)?;
            writeln!(
                source,
                "                if !({lhs} {} {}) {{",
                cmp_op(filter.op),
                numeric_literal_for_expr(&filter.lhs, filter.rhs.value)
            )
            .unwrap();
            writeln!(source, "                    candidate_complete = false;").unwrap();
            writeln!(source, "                }}").unwrap();
        }
        writeln!(source, "                if candidate_complete {{").unwrap();
        writeln!(
            source,
            "                    let constituents = Box::leak({used_ident}.into_boxed_slice());"
        )
        .unwrap();
        writeln!(
            source,
            "                    {derived_ident} = Some({derived_type} {{ mass, pt, min_delta_r, energy, px, py, pz, constituents }});"
        )
        .unwrap();
        writeln!(source, "                }}").unwrap();
        writeln!(source, "            }}").unwrap();
        writeln!(source, "        }}").unwrap();
        writeln!(source).unwrap();
        Ok(())
    }

    fn emit_candidate_filter_expr(
        &self,
        expr: &Expr,
        used_ident: &str,
    ) -> Result<String, CodegenError> {
        match expr {
            Expr::CandidateMinDeltaR => Ok(format!("gen_candidate_min_delta_r(&{used_ident})")),
            Expr::CandidateLeadingPt => Ok(format!("gen_candidate_leading_pt(&{used_ident})")),
            Expr::CandidateSubleadingPt => {
                Ok(format!("gen_candidate_subleading_pt(&{used_ident})"))
            }
            other => Err(CodegenError::UnsupportedFeature(format!(
                "candidate filter expression `{other}` is not supported by codegen"
            ))),
        }
    }

    fn emit_cut(&self, object: &ObjectDef, cut: &crate::Cut) -> Result<String, CodegenError> {
        let lhs = self.emit_cut_value_expr(&cut.lhs, &object.name)?;
        let op = cmp_op(cut.op);
        let rhs = numeric_literal_for_expr(&cut.lhs, cut.rhs.value);
        Ok(format!("({lhs} {op} {rhs})"))
    }

    fn emit_cut_value_expr(&self, expr: &Expr, object_name: &str) -> Result<String, CodegenError> {
        match expr {
            Expr::Attr { object, attr } if object == object_name => {
                attr_ident(object_name, attr)
            }
            Expr::Attr { object, .. } => Err(CodegenError::UnsupportedFeature(format!(
                "object cut references `{object}`; this slice only supports cuts on the object being selected"
            ))),
            Expr::Literal(value) => Ok(f64_literal(*value)),
            Expr::Binary { op, lhs, rhs } => {
                let lhs = as_f64_expr(self.emit_cut_value_expr(lhs, object_name)?);
                let rhs = as_f64_expr(self.emit_cut_value_expr(rhs, object_name)?);
                if matches!(op, ArithOp::Pow) {
                    Ok(format!("{lhs}.powf({rhs})"))
                } else {
                    Ok(format!("({lhs} {} {rhs})", arith_op(*op)))
                }
            }
            Expr::Abs(inner) => Ok(format!("{}.abs()", self.emit_cut_value_expr(inner, object_name)?)),
            Expr::Sqrt(inner) => Ok(format!(
                "{}.sqrt()",
                as_f64_expr(self.emit_cut_value_expr(inner, object_name)?)
            )),
            other => Err(CodegenError::UnsupportedFeature(format!(
                "object cut expression `{other}` is not supported by this slice"
            ))),
        }
    }

    fn emit_selected_predicate(
        &self,
        object: &str,
        item_ident: &str,
        predicate: &Cut,
    ) -> Result<String, CodegenError> {
        let lhs = self.emit_selected_value_expr(&predicate.lhs, object, item_ident)?;
        Ok(format!(
            "({} {} {})",
            as_f64_expr(lhs),
            cmp_op(predicate.op),
            f64_literal(predicate.rhs.value)
        ))
    }

    fn emit_selected_value_expr(
        &self,
        expr: &Expr,
        object_name: &str,
        item_ident: &str,
    ) -> Result<String, CodegenError> {
        match expr {
            Expr::Attr { object, attr } if object == object_name => Ok(format!(
                "{item_ident}.{}",
                checked_ident(attr, "selected attribute")?
            )),
            Expr::Attr { object, .. } => Err(CodegenError::UnsupportedFeature(format!(
                "collection predicate for `{object_name}` references `{object}`"
            ))),
            Expr::Literal(value) => Ok(f64_literal(*value)),
            Expr::Binary { op, lhs, rhs } => {
                let lhs =
                    as_f64_expr(self.emit_selected_value_expr(lhs, object_name, item_ident)?);
                let rhs =
                    as_f64_expr(self.emit_selected_value_expr(rhs, object_name, item_ident)?);
                if matches!(op, ArithOp::Pow) {
                    Ok(format!("{lhs}.powf({rhs})"))
                } else {
                    Ok(format!("({lhs} {} {rhs})", arith_op(*op)))
                }
            }
            Expr::Abs(inner) => Ok(format!(
                "{}.abs()",
                self.emit_selected_value_expr(inner, object_name, item_ident)?
            )),
            Expr::Sqrt(inner) => Ok(format!(
                "{}.sqrt()",
                as_f64_expr(self.emit_selected_value_expr(inner, object_name, item_ident)?)
            )),
            other => Err(CodegenError::UnsupportedFeature(format!(
                "collection predicate expression `{other}` is not supported by codegen"
            ))),
        }
    }

    fn emit_region_requirement(
        &self,
        lhs: &Expr,
        op: CmpOp,
        rhs: f64,
    ) -> Result<String, CodegenError> {
        match lhs {
            Expr::Count(object) => {
                let lhs = count_ident(object)?;
                let rhs = checked_count_rhs(rhs, "region requirement")?;
                Ok(format!("{lhs} {} {rhs}_u32", cmp_op(op)))
            }
            Expr::CountWhere { object, predicate } => {
                let selected = selected_objects_ident(object)?;
                let item = format!("{}_count_item", checked_ident(object, "count object")?);
                let predicate = self.emit_selected_predicate(object, &item, predicate)?;
                let rhs = checked_count_rhs(rhs, "region requirement")?;
                Ok(format!(
                    "({selected}.iter().filter(|{item}| {predicate}).count() as u32) {} {rhs}_u32",
                    cmp_op(op)
                ))
            }
            Expr::SumAttr { object, attr } => {
                let selected = selected_objects_ident(object)?;
                let item = format!("{}_sum_item", checked_ident(object, "sum object")?);
                let attr = checked_ident(attr, "sum attribute")?;
                Ok(format!(
                    "{selected}.iter().map(|{item}| {item}.{attr} as f64).sum::<f64>() {} {}",
                    cmp_op(op),
                    f64_literal(rhs)
                ))
            }
            Expr::All { object, predicate } => {
                let predicate = self.emit_collection_bool_expr("all", object, predicate)?;
                Ok(bool_comparison_expr(
                    predicate,
                    op,
                    rhs,
                    "region requirement",
                )?)
            }
            Expr::Any { object, predicate } => {
                let predicate = self.emit_collection_bool_expr("any", object, predicate)?;
                Ok(bool_comparison_expr(
                    predicate,
                    op,
                    rhs,
                    "region requirement",
                )?)
            }
            Expr::EitherPairPt {
                left,
                right,
                leading,
                subleading,
            } => {
                let predicate =
                    self.emit_either_pair_pt_expr(left, right, leading.value, subleading.value)?;
                Ok(bool_comparison_expr(
                    predicate,
                    op,
                    rhs,
                    "region requirement",
                )?)
            }
            Expr::ClosestMass {
                left,
                right,
                target,
            } => Ok(format!(
                "{} {} {}",
                self.emit_ordered_mass_expr(left, right, target.value, true)?,
                cmp_op(op),
                f64_literal(rhs)
            )),
            Expr::OtherMass {
                left,
                right,
                target,
            } => Ok(format!(
                "{} {} {}",
                self.emit_ordered_mass_expr(left, right, target.value, false)?,
                cmp_op(op),
                f64_literal(rhs)
            )),
            Expr::Attr { object, attr } if self.derived_object(object).is_ok() => {
                let lhs = derived_attr_expr(object, attr)?;
                Ok(format!("{lhs} {} {}", cmp_op(op), f64_literal(rhs)))
            }
            other => Err(CodegenError::UnsupportedFeature(format!(
                "region requirement `{other}` is not supported by this slice"
            ))),
        }
    }

    fn emit_collection_bool_expr(
        &self,
        method: &str,
        object: &str,
        predicate: &Cut,
    ) -> Result<String, CodegenError> {
        let selected = selected_objects_ident(object)?;
        let item = format!(
            "{}_{}_item",
            checked_ident(object, "predicate object")?,
            method
        );
        let predicate = self.emit_selected_predicate(object, &item, predicate)?;
        Ok(format!("{selected}.iter().{method}(|{item}| {predicate})"))
    }

    fn emit_either_pair_pt_expr(
        &self,
        left: &str,
        right: &str,
        leading: f64,
        subleading: f64,
    ) -> Result<String, CodegenError> {
        Ok(format!(
            "({} || {})",
            self.emit_pair_pt_expr(left, leading, subleading)?,
            self.emit_pair_pt_expr(right, leading, subleading)?
        ))
    }

    fn emit_pair_pt_expr(
        &self,
        object: &str,
        leading: f64,
        subleading: f64,
    ) -> Result<String, CodegenError> {
        let selected = selected_objects_ident(object)?;
        let item = format!("{}_pt_item", checked_ident(object, "pair pT object")?);
        Ok(format!(
            "{{ let mut pts = {selected}.iter().map(|{item}| {item}.pt).collect::<Vec<_>>(); pts.sort_by(|left, right| right.total_cmp(left)); pts.first().is_some_and(|pt| *pt > {}) && pts.get(1).is_some_and(|pt| *pt > {}) }}",
            f32_literal(leading),
            f32_literal(subleading)
        ))
    }

    fn emit_ordered_mass_expr(
        &self,
        left: &str,
        right: &str,
        target: f64,
        closest: bool,
    ) -> Result<String, CodegenError> {
        let left = checked_ident(left, "mass-order left")?;
        let right = checked_ident(right, "mass-order right")?;
        let target = f64_literal(target);
        let comparator = if closest { "<" } else { ">=" };
        Ok(format!(
            "(if ({left}.mass - {target}).abs() {comparator} ({right}.mass - {target}).abs() {{ {left}.mass }} else {{ {right}.mass }})"
        ))
    }

    fn emit_output_expr(&self, expr: &Expr, output_name: &str) -> Result<String, CodegenError> {
        match expr {
            Expr::Count(object) => count_ident(object),
            Expr::CountWhere { object, predicate } => {
                let selected = selected_objects_ident(object)?;
                let item = format!("{}_count_item", checked_ident(object, "count object")?);
                let predicate = self.emit_selected_predicate(object, &item, predicate)?;
                Ok(format!(
                    "{selected}.iter().filter(|{item}| {predicate}).count() as u32"
                ))
            }
            Expr::SumAttr { object, attr } => {
                let selected = selected_objects_ident(object)?;
                let item = format!("{}_sum_item", checked_ident(object, "sum object")?);
                let attr = checked_ident(attr, "sum attribute")?;
                Ok(format!(
                    "{selected}.iter().map(|{item}| {item}.{attr} as f64).sum::<f64>()"
                ))
            }
            Expr::All { object, predicate } => {
                self.emit_collection_bool_expr("all", object, predicate)
            }
            Expr::Any { object, predicate } => {
                self.emit_collection_bool_expr("any", object, predicate)
            }
            Expr::EitherPairPt {
                left,
                right,
                leading,
                subleading,
            } => self.emit_either_pair_pt_expr(left, right, leading.value, subleading.value),
            Expr::ClosestMass {
                left,
                right,
                target,
            } => self.emit_ordered_mass_expr(left, right, target.value, true),
            Expr::OtherMass {
                left,
                right,
                target,
            } => self.emit_ordered_mass_expr(left, right, target.value, false),
            Expr::Attr { object, attr } if self.derived_object(object).is_ok() => {
                derived_attr_expr(object, attr)
            }
            Expr::LeadingAttr { .. } => Ok(output_name.to_string()),
            other => Err(CodegenError::UnsupportedFeature(format!(
                "output expression `{other}` is not supported by this slice"
            ))),
        }
    }

    fn output_type(&self, expr: &Expr) -> Result<&'static str, CodegenError> {
        match expr {
            Expr::Count(_) => Ok("u32"),
            Expr::CountWhere { .. } => Ok("u32"),
            Expr::SumAttr { .. } => Ok("f64"),
            Expr::All { .. } | Expr::Any { .. } => Ok("bool"),
            Expr::EitherPairPt { .. } => Ok("bool"),
            Expr::ClosestMass { .. } | Expr::OtherMass { .. } => Ok("f64"),
            Expr::Attr { object, .. } if self.derived_object(object).is_ok() => Ok("f64"),
            Expr::LeadingAttr { .. } => Ok("f32"),
            other => Err(CodegenError::UnsupportedFeature(format!(
                "output expression `{other}` is not supported by this slice"
            ))),
        }
    }

    fn validate_cut_expr(&self, object_name: &str, expr: &Expr) -> Result<(), CodegenError> {
        match expr {
            Expr::Attr { object, attr } if object == object_name => {
                self.require_f32_attr(object, attr, "object cut")?;
                Ok(())
            }
            Expr::Attr { object, .. } => Err(CodegenError::UnsupportedFeature(format!(
                "object `{object_name}` cut references `{object}`; this slice only supports cuts on the object being selected"
            ))),
            Expr::Literal(_) => Ok(()),
            Expr::Binary { lhs, rhs, .. } => {
                self.validate_cut_expr(object_name, lhs)?;
                self.validate_cut_expr(object_name, rhs)
            }
            Expr::Abs(inner) => self.validate_cut_expr(object_name, inner),
            Expr::Sqrt(inner) => self.validate_cut_expr(object_name, inner),
            other => Err(CodegenError::UnsupportedFeature(format!(
                "object cut expression `{other}` is not supported by this slice"
            ))),
        }
    }

    fn validate_region_expr(&self, expr: &Expr, context: &str) -> Result<(), CodegenError> {
        match expr {
            Expr::Count(object) => {
                self.object(object)?;
                Ok(())
            }
            Expr::CountWhere { object, predicate }
            | Expr::All { object, predicate }
            | Expr::Any { object, predicate } => {
                self.object(object)?;
                self.validate_selected_expr(object, &predicate.lhs)
            }
            Expr::EitherPairPt { left, right, .. } => {
                self.object(left)?;
                self.object(right)?;
                self.require_f32_attr(left, "pt", context)?;
                self.require_f32_attr(right, "pt", context)
            }
            Expr::ClosestMass { left, right, .. } | Expr::OtherMass { left, right, .. } => {
                self.validate_derived_attr(left, "mass", context)?;
                self.validate_derived_attr(right, "mass", context)
            }
            Expr::SumAttr { object, attr } => {
                self.object(object)?;
                self.require_supported_object_attr(object, attr, context)
            }
            Expr::Attr { object, attr } if self.derived_object(object).is_ok() => {
                self.validate_derived_attr(object, attr, context)
            }
            other => Err(CodegenError::UnsupportedFeature(format!(
                "{context} expression `{other}` is not supported by this slice"
            ))),
        }
    }

    fn validate_selected_expr(&self, object_name: &str, expr: &Expr) -> Result<(), CodegenError> {
        match expr {
            Expr::Attr { object, attr } if object == object_name => {
                self.require_supported_object_attr(object, attr, "collection predicate")
            }
            Expr::Attr { object, .. } => Err(CodegenError::UnsupportedFeature(format!(
                "collection predicate for `{object_name}` references `{object}`"
            ))),
            Expr::Literal(_) => Ok(()),
            Expr::Binary { lhs, rhs, .. } => {
                self.validate_selected_expr(object_name, lhs)?;
                self.validate_selected_expr(object_name, rhs)
            }
            Expr::Abs(inner) | Expr::Sqrt(inner) => self.validate_selected_expr(object_name, inner),
            other => Err(CodegenError::UnsupportedFeature(format!(
                "collection predicate expression `{other}` is not supported by codegen"
            ))),
        }
    }

    fn require_f32_attr(
        &self,
        object_name: &str,
        attr: &str,
        context: &str,
    ) -> Result<(), CodegenError> {
        let object = self.object(object_name)?;
        if self
            .spec()
            .models
            .iter()
            .any(|model| self.model_output_matches_object_attr(model, &object.source, attr))
        {
            return Ok(());
        }
        let branch = format!("{}_{}", object.source, attr);
        let Some(info) = self.plan.read_branches.find(&branch) else {
            return Err(CodegenError::UnsupportedFeature(format!(
                "{context}: branch `{branch}` was not present in the validated read schema"
            )));
        };
        if info.branch_type != BranchType::VecF32 {
            return Err(CodegenError::UnsupportedFeature(format!(
                "{context}: branch `{branch}` has type {:?}; this slice only emits f32 object reads",
                info.branch_type
            )));
        }
        Ok(())
    }

    fn require_supported_object_attr(
        &self,
        object_name: &str,
        attr: &str,
        context: &str,
    ) -> Result<(), CodegenError> {
        let branch_type = self.object_attr_branch_type(object_name, attr)?;
        if !matches!(branch_type, BranchType::VecF32 | BranchType::VecI32) {
            let object = self.object(object_name)?;
            let branch = format!("{}_{}", object.source, attr);
            return Err(CodegenError::UnsupportedFeature(format!(
                "{context}: branch `{branch}` has type {:?}; this slice only emits f32/i32 object reads",
                branch_type
            )));
        }
        Ok(())
    }

    fn object_attr_rust_type(
        &self,
        object_name: &str,
        attr: &str,
    ) -> Result<&'static str, CodegenError> {
        match self.object_attr_branch_type(object_name, attr)? {
            BranchType::VecF32 => Ok("f32"),
            BranchType::VecI32 => Ok("i32"),
            other => Err(CodegenError::UnsupportedFeature(format!(
                "object `{object_name}` attribute `{attr}` has unsupported type {other:?}"
            ))),
        }
    }

    fn object_attr_branch_type(
        &self,
        object_name: &str,
        attr: &str,
    ) -> Result<BranchType, CodegenError> {
        let object = self.object(object_name)?;
        let branch = format!("{}_{}", object.source, attr);
        self.branch_type(&branch)
    }

    fn validate_derived_object(&self, derived: &DerivedObjectDef) -> Result<(), CodegenError> {
        match &derived.source {
            DerivedSource::Pair(pair) => {
                self.object(&pair.object)?;
                for attr in ["pt", "eta", "phi", "mass"] {
                    if self.object_attr_branch_type(&pair.object, attr)? != BranchType::VecF32 {
                        return Err(CodegenError::UnsupportedFeature(format!(
                            "derived pair `{}` requires `{}`.{attr} as a f32 vector branch",
                            derived.name, pair.object
                        )));
                    }
                }
                for constraint in &pair.constraints {
                    if matches!(constraint, PairConstraint::OppositeCharge)
                        && self.object_attr_branch_type(&pair.object, "charge")?
                            != BranchType::VecI32
                    {
                        return Err(CodegenError::UnsupportedFeature(format!(
                            "derived pair `{}` requires `{}`.charge as an i32 vector branch",
                            derived.name, pair.object
                        )));
                    }
                }
                for filter in &pair.filters {
                    self.validate_pair_filter(&pair.object, &filter.lhs)?;
                }
                for excluded in &pair.exclude {
                    self.derived_object(excluded)?;
                }
                Ok(())
            }
            DerivedSource::Candidate(candidate) => {
                if candidate.items.is_empty() {
                    return Err(CodegenError::UnsupportedFeature(format!(
                        "derived candidate `{}` has no items",
                        derived.name
                    )));
                }
                for item in &candidate.items {
                    if self.object(item).is_ok() {
                        for attr in ["pt", "eta", "phi", "mass"] {
                            if self.object_attr_branch_type(item, attr)? != BranchType::VecF32 {
                                return Err(CodegenError::UnsupportedFeature(format!(
                                    "derived candidate `{}` requires `{}`.{attr} as a f32 vector branch",
                                    derived.name, item
                                )));
                            }
                        }
                    } else {
                        self.derived_object(item)?;
                    }
                }
                for filter in &candidate.filters {
                    self.validate_candidate_filter(&filter.lhs)?;
                }
                Ok(())
            }
        }
    }

    fn validate_pair_filter(&self, object: &str, expr: &Expr) -> Result<(), CodegenError> {
        match expr {
            Expr::PairDeltaR => {
                self.require_f32_attr(object, "eta", "pair delta-R filter")?;
                self.require_f32_attr(object, "phi", "pair delta-R filter")
            }
            Expr::PairLeadingPt | Expr::PairSubleadingPt => {
                self.require_f32_attr(object, "pt", "pair pT filter")
            }
            other => Err(CodegenError::UnsupportedFeature(format!(
                "pair filter expression `{other}` is not supported by codegen"
            ))),
        }
    }

    fn validate_candidate_filter(&self, expr: &Expr) -> Result<(), CodegenError> {
        match expr {
            Expr::CandidateMinDeltaR | Expr::CandidateLeadingPt | Expr::CandidateSubleadingPt => {
                Ok(())
            }
            other => Err(CodegenError::UnsupportedFeature(format!(
                "candidate filter expression `{other}` is not supported by codegen"
            ))),
        }
    }

    fn validate_derived_attr(
        &self,
        object: &str,
        attr: &str,
        context: &str,
    ) -> Result<(), CodegenError> {
        self.derived_object(object)?;
        match attr {
            "mass" | "pt" | "min_delta_r" | "dR" | "dr" => Ok(()),
            other => Err(CodegenError::UnsupportedFeature(format!(
                "{context}: derived object `{object}` has no supported attribute `{other}`"
            ))),
        }
    }

    fn object(&self, name: &str) -> Result<&ObjectDef, CodegenError> {
        self.spec()
            .objects
            .iter()
            .find(|object| object.name == name)
            .ok_or_else(|| {
                CodegenError::UnsupportedFeature(format!(
                    "object `{name}` is not defined in the analysis spec"
                ))
            })
    }

    fn derived_object(&self, name: &str) -> Result<&DerivedObjectDef, CodegenError> {
        self.spec()
            .derived_objects
            .iter()
            .find(|object| object.name == name)
            .ok_or_else(|| {
                CodegenError::UnsupportedFeature(format!(
                    "derived object `{name}` is not defined in the analysis spec"
                ))
            })
    }

    fn ordered_derived_objects(&self) -> Result<Vec<&DerivedObjectDef>, CodegenError> {
        let mut ordered = Vec::with_capacity(self.spec().derived_objects.len());
        let mut visiting = BTreeSet::new();
        let mut visited = BTreeSet::new();
        for derived in &self.spec().derived_objects {
            self.visit_derived(derived, &mut visiting, &mut visited, &mut ordered)?;
        }
        Ok(ordered)
    }

    fn visit_derived<'b>(
        &'b self,
        derived: &'b DerivedObjectDef,
        visiting: &mut BTreeSet<String>,
        visited: &mut BTreeSet<String>,
        ordered: &mut Vec<&'b DerivedObjectDef>,
    ) -> Result<(), CodegenError> {
        if visited.contains(&derived.name) {
            return Ok(());
        }
        if !visiting.insert(derived.name.clone()) {
            return Err(CodegenError::UnsupportedFeature(format!(
                "derived object dependency cycle includes `{}`",
                derived.name
            )));
        }
        for dependency in derived_dependencies(derived) {
            if let Ok(dep) = self.derived_object(&dependency) {
                self.visit_derived(dep, visiting, visited, ordered)?;
            }
        }
        visiting.remove(&derived.name);
        visited.insert(derived.name.clone());
        ordered.push(derived);
        Ok(())
    }

    fn validate_model_region_expr(&self, expr: &Expr, context: &str) -> Result<(), CodegenError> {
        match expr {
            Expr::Count(object) => {
                self.object(object)?;
                Ok(())
            }
            Expr::LeadingAttr { object, attr } => self.require_f32_attr(object, attr, context),
            other => Err(CodegenError::UnsupportedFeature(format!(
                "{context} expression `{other}` is not supported by model-aware codegen"
            ))),
        }
    }

    fn emit_model_tag(&self, source: &mut String, model: &ModelDef) -> Result<(), CodegenError> {
        let type_ident = self.model_type_ident(model)?;
        let batch = self.model_batch_source(model)?;
        let (_, output_attr) = split_model_output(&model.output)?;

        writeln!(source).unwrap();
        writeln!(
            source,
            "#[derive(Debug, Clone, Copy, Default, PartialEq, Eq)]"
        )
        .unwrap();
        writeln!(source, "struct {type_ident};").unwrap();
        writeln!(source).unwrap();
        writeln!(source, "impl nano_analysis::ModelTag for {type_ident} {{").unwrap();
        writeln!(
            source,
            "    const NAME: &'static str = {};",
            rust_string(&model.name)
        )
        .unwrap();
        writeln!(
            source,
            "    const BATCH: &'static str = {};",
            rust_string(&batch)
        )
        .unwrap();
        writeln!(
            source,
            "    const OUTPUT: &'static str = {};",
            rust_string(output_attr)
        )
        .unwrap();
        writeln!(source, "}}").unwrap();
        Ok(())
    }

    fn emit_model_inference(
        &self,
        source: &mut String,
        model: &ModelDef,
    ) -> Result<(), CodegenError> {
        let model_ident = checked_ident(&model.name, "model name")?;
        let type_ident = self.model_type_ident(model)?;
        let batch = self.model_batch_source(model)?;
        let n_inputs = model.inputs.len();

        writeln!(
            source,
            "        let {model_ident}_collection = event.collection({}).map_err(gen_core_error)?;",
            rust_string(&batch)
        )
        .unwrap();
        writeln!(
            source,
            "        let mut {model_ident}_values = Vec::with_capacity({model_ident}_collection.len() * {n_inputs});"
        )
        .unwrap();
        writeln!(
            source,
            "        for {model_ident}_item in {model_ident}_collection.iter() {{"
        )
        .unwrap();
        for input in &model.inputs {
            self.emit_model_input_push(source, model, input)?;
        }
        writeln!(source, "        }}").unwrap();
        writeln!(
            source,
            "        let {model_ident}_features = nano_analysis::Features::<{type_ident}>::from_tensors(vec![nano_inference::Tensor {{"
        )
        .unwrap();
        writeln!(source, "            name: \"features\".to_string(),").unwrap();
        writeln!(
            source,
            "            shape: vec![{model_ident}_collection.len(), {n_inputs}],"
        )
        .unwrap();
        writeln!(
            source,
            "            data: nano_inference::TensorData::F32({model_ident}_values),"
        )
        .unwrap();
        writeln!(source, "        }}]);").unwrap();
        writeln!(
            source,
            "        let {model_ident}_baseline = nano_analysis::Ev::new(event)"
        )
        .unwrap();
        writeln!(source, "            .preselect(|_| true)").unwrap();
        writeln!(
            source,
            "            .expect(\"true generated preselection should always pass\");"
        )
        .unwrap();
        writeln!(
            source,
            "        let _{model_ident}_scored = {model_ident}_baseline.infer::<{type_ident}>(predictor, {model_ident}_features)?;"
        )
        .unwrap();
        writeln!(source).unwrap();
        Ok(())
    }

    fn emit_model_input_push(
        &self,
        source: &mut String,
        model: &ModelDef,
        input: &str,
    ) -> Result<(), CodegenError> {
        let model_ident = checked_ident(&model.name, "model name")?;
        let local_ident = format!("{model_ident}_{}", branch_ident(input)?);
        let branch_type = self.branch_type(input)?;
        if branch_type.is_vector() {
            let batch = self.model_batch_source(model)?;
            let Some((source_name, attr)) = input.split_once('_') else {
                return Err(CodegenError::UnsupportedFeature(format!(
                    "model `{}` input `{input}` is not a supported object attribute branch",
                    model.name
                )));
            };
            if source_name != batch {
                return Err(CodegenError::UnsupportedFeature(format!(
                    "model `{}` input `{input}` is a vector branch outside batch `{batch}`",
                    model.name
                )));
            }
            let value_expr = match branch_type {
                BranchType::VecI8 => format!(
                    "{model_ident}_item.get::<i8>({}).map(|value| value as f32)",
                    rust_string(attr)
                ),
                BranchType::VecU8 => format!(
                    "{model_ident}_item.get::<u8>({}).map(|value| value as f32)",
                    rust_string(attr)
                ),
                BranchType::VecI16 => format!(
                    "{model_ident}_item.get::<i16>({}).map(|value| value as f32)",
                    rust_string(attr)
                ),
                BranchType::VecU16 => format!(
                    "{model_ident}_item.get::<u16>({}).map(|value| value as f32)",
                    rust_string(attr)
                ),
                BranchType::VecI32 => format!(
                    "{model_ident}_item.get::<i32>({}).map(|value| value as f32)",
                    rust_string(attr)
                ),
                BranchType::VecU32 => format!(
                    "{model_ident}_item.get::<u32>({}).map(|value| value as f32)",
                    rust_string(attr)
                ),
                BranchType::VecI64 => format!(
                    "{model_ident}_item.get::<i64>({}).map(|value| value as f32)",
                    rust_string(attr)
                ),
                BranchType::VecU64 => format!(
                    "{model_ident}_item.get::<u64>({}).map(|value| value as f32)",
                    rust_string(attr)
                ),
                BranchType::VecF32 => {
                    format!("{model_ident}_item.get::<f32>({})", rust_string(attr))
                }
                other => {
                    return Err(CodegenError::UnsupportedFeature(format!(
                        "model `{}` input `{input}` has unsupported type {other:?}",
                        model.name
                    )));
                }
            };
            writeln!(
                source,
                "            let {local_ident} = {value_expr}.map_err(gen_core_error)?;"
            )
            .unwrap();
        } else {
            let value_expr = match branch_type {
                BranchType::I8 => format!(
                    "event.scalar::<i8>({}).map(|value| value as f32)",
                    rust_string(input)
                ),
                BranchType::U8 => format!(
                    "event.scalar::<u8>({}).map(|value| value as f32)",
                    rust_string(input)
                ),
                BranchType::I16 => format!(
                    "event.scalar::<i16>({}).map(|value| value as f32)",
                    rust_string(input)
                ),
                BranchType::U16 => format!(
                    "event.scalar::<u16>({}).map(|value| value as f32)",
                    rust_string(input)
                ),
                BranchType::I32 => format!(
                    "event.scalar::<i32>({}).map(|value| value as f32)",
                    rust_string(input)
                ),
                BranchType::U32 => format!(
                    "event.scalar::<u32>({}).map(|value| value as f32)",
                    rust_string(input)
                ),
                BranchType::I64 => format!(
                    "event.scalar::<i64>({}).map(|value| value as f32)",
                    rust_string(input)
                ),
                BranchType::U64 => format!(
                    "event.scalar::<u64>({}).map(|value| value as f32)",
                    rust_string(input)
                ),
                BranchType::F32 => format!("event.scalar::<f32>({})", rust_string(input)),
                other => {
                    return Err(CodegenError::UnsupportedFeature(format!(
                        "model `{}` input `{input}` has unsupported type {other:?}",
                        model.name
                    )));
                }
            };
            writeln!(
                source,
                "            let {local_ident} = {value_expr}.map_err(gen_core_error)?;"
            )
            .unwrap();
        }
        writeln!(
            source,
            "            {model_ident}_values.push({local_ident});"
        )
        .unwrap();
        Ok(())
    }

    fn emit_model_object_selection(
        &self,
        source: &mut String,
        object_name: &str,
    ) -> Result<(), CodegenError> {
        let object = self.object(object_name)?;
        let object_name = checked_ident(&object.name, "object name")?;
        let collection_ident = format!("{object_name}_collection");
        let item_ident = format!("{object_name}_item");
        let count_ident = count_ident(&object.name)?;
        let attrs = self.attrs_needed_for_object(&object.name)?;

        writeln!(
            source,
            "        let {collection_ident} = event.collection({}).map_err(gen_core_error)?;",
            rust_string(&object.source)
        )
        .unwrap();
        writeln!(source, "        let mut {count_ident} = 0_u32;").unwrap();

        for (object, attr) in self.leading_exprs_for_object(&object.name)? {
            let ident = leading_attr_ident(&object, &attr)?;
            writeln!(source, "        let mut {ident}: Option<f32> = None;").unwrap();
        }

        writeln!(source).unwrap();
        writeln!(
            source,
            "        for {item_ident} in {collection_ident}.iter() {{"
        )
        .unwrap();

        for attr in attrs {
            self.require_f32_attr(&object.name, &attr, "object attribute")?;
            let attr_ident = attr_ident(&object.name, &attr)?;
            writeln!(
                source,
                "            let {attr_ident} = {item_ident}.get::<f32>({}).map_err(gen_core_error)?;",
                rust_string(&attr)
            )
            .unwrap();
        }

        let condition = if object.cuts.is_empty() {
            "true".to_string()
        } else {
            object
                .cuts
                .iter()
                .map(|cut| self.emit_cut(object, cut))
                .collect::<Result<Vec<_>, _>>()?
                .join(" && ")
        };

        writeln!(source, "            if {condition} {{").unwrap();
        writeln!(source, "                {count_ident} += 1;").unwrap();

        for (object, attr) in self.leading_exprs_for_object(&object.name)? {
            let leading_ident = leading_attr_ident(&object, &attr)?;
            let attr_ident = attr_ident(&object, &attr)?;
            writeln!(source, "                {leading_ident} = Some(").unwrap();
            writeln!(
                source,
                "                    {leading_ident}.map_or({attr_ident}, |lead| lead.max({attr_ident})),"
            )
            .unwrap();
            writeln!(source, "                );").unwrap();
        }

        writeln!(source, "            }}").unwrap();
        writeln!(source, "        }}").unwrap();
        writeln!(source).unwrap();
        Ok(())
    }

    fn emit_model_region_requirement(
        &self,
        lhs: &Expr,
        op: CmpOp,
        rhs: f64,
        source: &mut String,
        unwrapped_leading: &mut BTreeSet<(String, String)>,
    ) -> Result<String, CodegenError> {
        match lhs {
            Expr::Count(object) => {
                let lhs = count_ident(object)?;
                let rhs = checked_count_rhs(rhs, "region requirement")?;
                Ok(format!("{lhs} {} {rhs}_u32", cmp_op(op)))
            }
            Expr::LeadingAttr { object, attr } => {
                let key = (object.clone(), attr.clone());
                let lhs = leading_attr_ident(object, attr)?;
                if unwrapped_leading.insert(key) {
                    writeln!(source, "        let Some({lhs}) = {lhs} else {{").unwrap();
                    writeln!(source, "            return Ok(None);").unwrap();
                    writeln!(source, "        }};").unwrap();
                }
                Ok(format!("{lhs} {} {}", cmp_op(op), f32_literal(rhs)))
            }
            other => Err(CodegenError::UnsupportedFeature(format!(
                "region requirement `{other}` is not supported by model-aware codegen"
            ))),
        }
    }

    fn emit_model_output_expr(&self, expr: &Expr) -> Result<String, CodegenError> {
        match expr {
            Expr::Count(object) => count_ident(object),
            Expr::LeadingAttr { object, attr } => leading_attr_ident(object, attr),
            other => Err(CodegenError::UnsupportedFeature(format!(
                "output expression `{other}` is not supported by model-aware codegen"
            ))),
        }
    }

    fn leading_region_exprs(&self) -> BTreeSet<(String, String)> {
        self.spec()
            .regions
            .iter()
            .flat_map(|region| &region.require)
            .filter_map(|requirement| match &requirement.lhs {
                Expr::LeadingAttr { object, attr } => Some((object.clone(), attr.clone())),
                _ => None,
            })
            .collect()
    }

    fn leading_exprs_for_object(
        &self,
        object_name: &str,
    ) -> Result<BTreeSet<(String, String)>, CodegenError> {
        let mut exprs = BTreeSet::new();
        for output in &self.spec().outputs {
            if let Expr::LeadingAttr { object, attr } = &output.expr {
                if object == object_name {
                    exprs.insert((object.clone(), attr.clone()));
                }
            }
        }
        for region in &self.spec().regions {
            for requirement in &region.require {
                if let Expr::LeadingAttr { object, attr } = &requirement.lhs {
                    if object == object_name {
                        exprs.insert((object.clone(), attr.clone()));
                    }
                }
            }
        }
        for (object, attr) in &exprs {
            self.require_f32_attr(object, attr, "leading attribute")?;
        }
        Ok(exprs)
    }

    fn model_type_ident(&self, model: &ModelDef) -> Result<String, CodegenError> {
        upper_camel_ident(&model.name, "model type")
    }

    fn model_batch_source(&self, model: &ModelDef) -> Result<String, CodegenError> {
        if let Some(object) = self
            .spec()
            .objects
            .iter()
            .find(|object| object.name == model.batch)
        {
            return Ok(object.source.clone());
        }
        if self
            .spec()
            .objects
            .iter()
            .any(|object| object.source == model.batch)
        {
            return Ok(model.batch.clone());
        }
        Err(CodegenError::UnsupportedFeature(format!(
            "model `{}` batch `{}` is not defined",
            model.name, model.batch
        )))
    }

    fn model_output_matches_object_attr(
        &self,
        model: &ModelDef,
        object_source: &str,
        attr: &str,
    ) -> bool {
        split_model_output(&model.output)
            .is_ok_and(|(source, output_attr)| source == object_source && output_attr == attr)
    }

    fn validate_model_input(&self, model: &ModelDef, input: &str) -> Result<(), CodegenError> {
        let branch_type = self.branch_type(input)?;
        if branch_type.is_vector() {
            let batch = self.model_batch_source(model)?;
            let Some((source, _)) = input.split_once('_') else {
                return Err(CodegenError::UnsupportedFeature(format!(
                    "model `{}` input `{input}` is not a supported object attribute branch",
                    model.name
                )));
            };
            if source != batch {
                return Err(CodegenError::UnsupportedFeature(format!(
                    "model `{}` input `{input}` is a vector branch outside batch `{batch}`",
                    model.name
                )));
            }
        }
        Ok(())
    }

    fn branch_type(&self, branch: &str) -> Result<BranchType, CodegenError> {
        self.plan
            .read_branches
            .find(branch)
            .map(|info| info.branch_type)
            .ok_or_else(|| {
                CodegenError::UnsupportedFeature(format!(
                    "branch `{branch}` was not present in the validated read schema"
                ))
            })
    }
}

fn collect_attr_names_for_current_object(
    expr: &Expr,
    object_name: &str,
    attrs: &mut BTreeSet<String>,
) -> Result<(), CodegenError> {
    match expr {
        Expr::Attr { object, attr } if object == object_name => {
            attrs.insert(attr.clone());
            Ok(())
        }
        Expr::Attr { object, .. } => Err(CodegenError::UnsupportedFeature(format!(
            "object `{object_name}` cut references `{object}`; this slice only supports cuts on the object being selected"
        ))),
        Expr::Literal(_) => Ok(()),
        Expr::Binary { lhs, rhs, .. } => {
            collect_attr_names_for_current_object(lhs, object_name, attrs)?;
            collect_attr_names_for_current_object(rhs, object_name, attrs)
        }
        Expr::Abs(inner) => collect_attr_names_for_current_object(inner, object_name, attrs),
        Expr::Sqrt(inner) => collect_attr_names_for_current_object(inner, object_name, attrs),
        other => Err(CodegenError::UnsupportedFeature(format!(
            "object cut expression `{other}` is not supported by this slice"
        ))),
    }
}

fn collect_selected_attr_names(
    expr: &Expr,
    object_name: &str,
    attrs: &mut BTreeSet<String>,
) -> Result<(), CodegenError> {
    match expr {
        Expr::Attr { object, attr } if object == object_name => {
            attrs.insert(attr.clone());
            Ok(())
        }
        Expr::Attr { .. } | Expr::Literal(_) | Expr::Count(_) | Expr::LeadingAttr { .. } => Ok(()),
        Expr::Binary { lhs, rhs, .. } => {
            collect_selected_attr_names(lhs, object_name, attrs)?;
            collect_selected_attr_names(rhs, object_name, attrs)
        }
        Expr::Abs(inner) | Expr::Sqrt(inner) => {
            collect_selected_attr_names(inner, object_name, attrs)
        }
        Expr::CountWhere { object, predicate }
        | Expr::All { object, predicate }
        | Expr::Any { object, predicate }
            if object == object_name =>
        {
            collect_selected_attr_names(&predicate.lhs, object_name, attrs)
        }
        Expr::CountWhere { .. } | Expr::All { .. } | Expr::Any { .. } => Ok(()),
        Expr::EitherPairPt { left, right, .. } => {
            if left == object_name || right == object_name {
                attrs.insert("pt".to_string());
            }
            Ok(())
        }
        Expr::ClosestMass { .. } | Expr::OtherMass { .. } => Ok(()),
        Expr::SumAttr { object, attr } if object == object_name => {
            attrs.insert(attr.clone());
            Ok(())
        }
        Expr::SumAttr { .. } => Ok(()),
        Expr::PairDeltaR
        | Expr::PairLeadingPt
        | Expr::PairSubleadingPt
        | Expr::CandidateMinDeltaR
        | Expr::CandidateLeadingPt
        | Expr::CandidateSubleadingPt => Ok(()),
    }
}

