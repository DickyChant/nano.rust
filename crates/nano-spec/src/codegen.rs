fn upper_camel_ident(value: &str, context: &str) -> Result<String, CodegenError> {
    checked_ident(value, context)?;
    let mut ident = String::new();
    for part in value.split('_') {
        if part.is_empty() {
            return Err(CodegenError::InvalidIdentifier {
                context: context.to_string(),
                value: value.to_string(),
            });
        }
        let mut chars = part.chars();
        let Some(first) = chars.next() else {
            return Err(CodegenError::InvalidIdentifier {
                context: context.to_string(),
                value: value.to_string(),
            });
        };
        ident.push(first.to_ascii_uppercase());
        ident.extend(chars);
    }
    checked_ident(&ident, context)
}

fn split_model_output(output: &str) -> Result<(&str, &str), CodegenError> {
    let Some((source, attr)) = output.split_once('_') else {
        return Err(CodegenError::UnsupportedFeature(format!(
            "model output `{output}` is not a supported per-object column"
        )));
    };
    Ok((source, attr))
}

fn checked_ident(value: &str, context: &str) -> Result<String, CodegenError> {
    let mut chars = value.chars();
    let Some(first) = chars.next() else {
        return Err(CodegenError::InvalidIdentifier {
            context: context.to_string(),
            value: value.to_string(),
        });
    };
    if !(first == '_' || first.is_ascii_alphabetic())
        || !chars.all(|ch| ch == '_' || ch.is_ascii_alphanumeric())
        || rust_keywords().contains_key(value)
    {
        return Err(CodegenError::InvalidIdentifier {
            context: context.to_string(),
            value: value.to_string(),
        });
    }
    Ok(value.to_string())
}

fn rust_keywords() -> BTreeMap<&'static str, ()> {
    [
        "as", "break", "const", "continue", "crate", "else", "enum", "extern", "false", "fn",
        "for", "if", "impl", "in", "let", "loop", "match", "mod", "move", "mut", "pub", "ref",
        "return", "self", "Self", "static", "struct", "super", "trait", "true", "type", "unsafe",
        "use", "where", "while", "async", "await", "dyn",
    ]
    .into_iter()
    .map(|keyword| (keyword, ()))
    .collect()
}

fn cmp_op(op: CmpOp) -> &'static str {
    match op {
        CmpOp::Gt => ">",
        CmpOp::Ge => ">=",
        CmpOp::Lt => "<",
        CmpOp::Le => "<=",
        CmpOp::Eq => "==",
        CmpOp::Ne => "!=",
    }
}

fn arith_op(op: ArithOp) -> &'static str {
    match op {
        ArithOp::Add => "+",
        ArithOp::Sub => "-",
        ArithOp::Mul => "*",
        ArithOp::Div => "/",
        ArithOp::Pow => ".powf",
    }
}

fn as_f64_expr(expr: String) -> String {
    format!("({expr} as f64)")
}

fn numeric_literal_for_expr(expr: &Expr, value: f64) -> String {
    match expr {
        Expr::PairLeadingPt
        | Expr::PairSubleadingPt
        | Expr::CandidateLeadingPt
        | Expr::CandidateSubleadingPt
        | Expr::Attr { .. }
        | Expr::Abs(_) => f32_literal(value),
        _ => f64_literal(value),
    }
}

fn bool_comparison_expr(
    predicate: String,
    op: CmpOp,
    rhs: f64,
    context: &str,
) -> Result<String, CodegenError> {
    match (op, rhs) {
        (CmpOp::Eq, 1.0) | (CmpOp::Ne, 0.0) => Ok(predicate),
        (CmpOp::Ne, 1.0) | (CmpOp::Eq, 0.0) => Ok(format!("!({predicate})")),
        _ => Err(CodegenError::UnsupportedFeature(format!(
            "{context}: boolean predicate comparisons support only == 1, != 0, == 0, or != 1"
        ))),
    }
}

fn f32_literal(value: f64) -> String {
    format!("{value:?}_f32")
}

fn f64_literal(value: f64) -> String {
    format!("{value:?}_f64")
}

fn checked_count_rhs(value: f64, context: &str) -> Result<u32, CodegenError> {
    if value.is_finite() && value >= 0.0 && value.fract() == 0.0 && value <= u32::MAX as f64 {
        Ok(value as u32)
    } else {
        Err(CodegenError::UnsupportedFeature(format!(
            "{context}: count comparison rhs `{value}` is not a non-negative u32 integer"
        )))
    }
}

fn rust_string(value: &str) -> String {
    format!("{value:?}")
}

fn collect_derived_objects_in_expr(
    expr: &Expr,
    spec: &AnalysisSpec,
    objects: &mut BTreeSet<String>,
) {
    match expr {
        Expr::Attr { object, .. }
            if spec
                .derived_objects
                .iter()
                .any(|derived| derived.name == *object) =>
        {
            objects.insert(object.clone());
        }
        Expr::Abs(inner) | Expr::Sqrt(inner) => {
            collect_derived_objects_in_expr(inner, spec, objects)
        }
        Expr::Binary { lhs, rhs, .. } => {
            collect_derived_objects_in_expr(lhs, spec, objects);
            collect_derived_objects_in_expr(rhs, spec, objects);
        }
        Expr::CountWhere { predicate, .. }
        | Expr::All { predicate, .. }
        | Expr::Any { predicate, .. } => {
            collect_derived_objects_in_expr(&predicate.lhs, spec, objects);
        }
        Expr::ClosestMass { left, right, .. } | Expr::OtherMass { left, right, .. } => {
            if spec
                .derived_objects
                .iter()
                .any(|derived| derived.name == *left)
            {
                objects.insert(left.clone());
            }
            if spec
                .derived_objects
                .iter()
                .any(|derived| derived.name == *right)
            {
                objects.insert(right.clone());
            }
        }
        _ => {}
    }
}

fn derived_dependencies(derived: &DerivedObjectDef) -> Vec<String> {
    match &derived.source {
        DerivedSource::Pair(pair) => pair.exclude.clone(),
        DerivedSource::Candidate(candidate) => candidate.items.clone(),
    }
}

fn expr_counts_object(expr: &Expr, object_name: &str) -> bool {
    match expr {
        Expr::Count(object) => object == object_name,
        Expr::CountWhere { object, .. } => object == object_name,
        Expr::Abs(inner) | Expr::Sqrt(inner) => expr_counts_object(inner, object_name),
        Expr::Binary { lhs, rhs, .. } => {
            expr_counts_object(lhs, object_name) || expr_counts_object(rhs, object_name)
        }
        _ => false,
    }
}

